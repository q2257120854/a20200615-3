	static $_TSPEC;
	public $total = null;
	public $poList = null;
	
	public function __construct($vals=null){
		
		if (!isset(self::$_TSPEC)){
			
			self::$_TSPEC = array(
			1 => array(
			'var' => 'total'
			),
			2 => array(
			'var' => 'poList'
			),
			
			);
			
		}
		
		if (is_array($vals)){
			
			
			if (isset($vals['total'])){
				
				$this->total = $vals['total'];
			}
			
			
			if (isset($vals['poList'])){
				
				$this->poList = $vals['poList'];
			}
			
			
		}
		
	}
	
	
	public function getName(){
		
		return 'SearchPoResponse';
	}
	

namespace com\vip\haitao\loading\osp\service;

class PostBody {
	
	static $_TSPEC;
	public $returnList = null;
	public $returnErrorList = null;
	
	public function __construct($vals=null){
		
		if (!isset(self::$_TSPEC)){
			
			self::$_TSPEC = array(
			1 => array(
			'var' => 'returnList'
			),
			2 => array(
			'var' => 'returnErrorList'
			),
			
			);
			
		}
		
		if (is_array($vals)){
			
			
			if (isset($vals['returnList'])){
				
				$this->returnList = $vals['returnList'];
			}
			
			
			if (isset($vals['returnErrorList'])){
				
				$this->returnErrorList = $vals['returnErrorList'];
			}
			
			
		}
		
	}
	
	
	public function getName(){
		
		return 'PostBody';
	}
	
	public function read($input){
		
		$input->readStructBegin();
		while(true){
			
			$schemeField = $input->readFieldBegin();
			if ($schemeField == null) break;
			$needSkip = true;
			
			
			if ("returnList" == $schemeField){
				
				$needSkip = false;
				
				$this->returnList = array();
				$_size0 = 0;
				$input->readListBegin();
				while(true){
					
					try{
						
						$elem0 = null;
						$input->readString($elem0);
						
						$this->returnList[$_size0++] = $elem0;
					}
					catch(\Exception $e){
						
						break;
					}
				}
				
				$input->readListEnd();
				
			}
			
			
			
			
			if ("returnErrorList" == $schemeField){
				
				$needSkip = false;
				
				$this->returnErrorList = array();
				$_size1 = 0;
				$input->readListBegin();
				while(true){
					
					try{
						
						$elem1 = null;
						
						$elem1 = new \com\vip\haitao\loading\osp\service\PostReturnError();
						$elem1->read($input);
						
						$this->returnErrorList[$_size1++] = $elem1;
					}
					catch(\Exception $e){
						
						break;
					}
				}
				
				$input->readListEnd();
				
			}
			
			
			
			if($needSkip){
				
				\Osp\Protocol\ProtocolUtil::skip($input);
			}
			
			$input->readFieldEnd();
		}
		
		$input->readStructEnd();
		
		
		
	}
	
	public function write($output){
		
		$xfer = 0;
		$xfer += $output->writeStructBegin();
		
		if($this->returnList !== null) {
			
			$xfer += $output->writeFieldBegin('returnList');
			
			if (!is_array($this->returnList)){
				
				throw new \Osp\Exception\OspException('Bad type in structure.', \Osp\Exception\OspException::INVALID_DATA);
			}
			
			$output->writeListBegin();
			foreach ($this->returnList as $iter0){
				
				$xfer += $output->writeString($iter0);
				
			}
			
			$output->writeListEnd();
			
			$xfer += $output->writeFieldEnd();
		}
		
		
		if($this->returnErrorList !== null) {
			
			$xfer += $output->writeFieldBegin('returnErrorList');
			
			if (!is_array($this->returnErrorList)){
				
				throw new \Osp\Exception\OspException('Bad type in structure.', \Osp\Exception\OspException::INVALID_DATA);
			}
			
			$output->writeListBegin();
			foreach ($this->returnErrorList as $iter0){
				
				
				if (!is_object($iter0)) {
					
					throw new \Osp\Exception\OspException('Bad type in structure.', \Osp\Exception\OspException::INVALID_DATA);
				}
				
				$xfer += $iter0->write($output);
				
			}
			
			$output->writeListEnd();
			
			$xfer += $output->writeFieldEnd();
		}
		
		
		$xfer += $output->writeFieldStop();
		$xfer += $output->writeStructEnd();
		return $xfer;
	}
	
}

